    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit(User $user)
    {
        //
        return view('admin.users.edit', ['user' => $user]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        //
        $user->fill($request->all())->update();
        return back()->withStatus(__('Profile successfully updated.'));
    }

    public function password(PasswordRequest $request, User $user)
    {
        $user->update(['password' => Hash::make($request->get('password'))]);
        return back()->withStatusPassword(__('Password successfully updated.'));
    }

    public function licenseChange(Request $request)
    {
        $user = User::where('id',$request->get('user_id'));
        $user->update([
            'license_paid' => $request->get('license_paid')
        ]);
        return back()->with('success','User License Activated');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(User $user)
    {
        //
        $user->delete();
        return back()->with('success','User Deleted');
    }
